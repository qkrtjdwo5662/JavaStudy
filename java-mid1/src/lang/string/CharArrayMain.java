package lang.string;

public class CharArrayMain {
    public static void main(String[] args) {
        char[] charArr = new char[]{'h', 'e', 'l', 'l', 'o'};
        System.out.println(charArr);

        String str = "hello";
        System.out.println(str);

        // char[] 대신 String을 통해 문자열 생성 가능
    }
}
